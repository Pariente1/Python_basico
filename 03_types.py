# string; es una cadena de texto. Se abrevia 'STR'
my_name = "Nicolas"
my_name = 'Santiago'
print('my_name =>', my_name)
print(type(my_name))

# integer; o numeros enteros se abrevia 'INT'
my_age= 12
print('my_age =>', my_age)
print(type(my_age))

# boolean; es falso o verdadero. Se abrevia 'BOOL'. Es importante que la primera letra sea en mayuscula False or True ya que son palabras reservadas de python. Si usas minusculas no funciona.

is_single = True
print('is_single =>', is_single)
print(type(is_single))

